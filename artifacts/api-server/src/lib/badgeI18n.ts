/**
 * Task #1752 — server-side translations for the player-facing badge `label`
 * and `description` strings returned by both
 *   GET /api/portal/my-badges          (mobile catalog screen)
 * and
 *   GET /api/public/p/:handle          (public profile + public badge page)
 *
 * Why server-side
 * ---------------
 * A previous wave (Task #1441/#1442) translated the screen chrome on the mobile
 * badges screen and the public badge landing page, but the badge titles and
 * descriptions themselves still came back from the API in English because they
 * lived in `ALL_BADGES` (see `achievementEngine.ts`). Adding ~600 new keys to
 * the mobile `profile.json` for every locale would balloon the bundle, fan out
 * across two clients (mobile + website), and create two parallel translation
 * tables to keep in sync. Translating once on the server keeps the catalog as
 * the single source of truth — both clients call the same endpoints, pass
 * `?lang=<player-locale>`, and receive ready-to-render copy.
 *
 * Resolution rules
 * ----------------
 *  1. `?lang=` query takes precedence (the mobile and web clients pass the
 *     player's selected i18n locale here so the JSON they render matches the
 *     surrounding chrome, even when the device locale would say otherwise).
 *  2. `Accept-Language` header is consulted next so a recipient who taps a
 *     share link without a `?lang=` override still gets their browser locale.
 *  3. Anything unknown falls back to English.
 *
 * Per-badge fallback
 * ------------------
 * `localizeBadge(def, lang)` returns the requested locale's translation when
 * present; missing entries fall back to English so a partially-translated
 * locale never renders a literal badge type slug.
 */

import type { Request } from "express";
import type { BadgeDef } from "./achievementEngine";

export type BadgeI18nLang =
  | "af" | "am" | "ar" | "de" | "en" | "es" | "fil" | "fr"
  | "ha" | "hi" | "id" | "ja" | "ko" | "ms" | "pt" | "sw"
  | "th" | "vi" | "yo" | "zh" | "zu";

export const BADGE_I18N_LANGS: BadgeI18nLang[] = [
  "af", "am", "ar", "de", "en", "es", "fil", "fr",
  "ha", "hi", "id", "ja", "ko", "ms", "pt", "sw",
  "th", "vi", "yo", "zh", "zu",
];

/**
 * The "core" set of locales that the lint test requires complete coverage
 * for. New badges added to `ALL_BADGES` must ship at minimum a translation
 * for every locale in this set or `badge-i18n-coverage.test.ts` fails. The
 * remaining locales fall back to English when missing.
 */
export const CORE_BADGE_I18N_LANGS: BadgeI18nLang[] = [
  "en", "hi", "ar", "es", "fr", "de", "pt", "ja", "ko", "zh", "th",
];

const SUPPORTED_SET = new Set<string>(BADGE_I18N_LANGS);

/** Collapse a possibly-loose locale string ("zh-Hant-TW", "pt_BR") to one
 *  of our supported codes. Returns `en` for anything unknown. */
export function normalizeBadgeI18nLang(input: string | null | undefined): BadgeI18nLang {
  if (!input) return "en";
  const lower = String(input).toLowerCase().trim();
  if (!lower) return "en";
  if (SUPPORTED_SET.has(lower)) return lower as BadgeI18nLang;
  const primary = lower.split(/[-_]/, 1)[0];
  if (primary && SUPPORTED_SET.has(primary)) return primary as BadgeI18nLang;
  return "en";
}

/**
 * Pick the best supported locale for an HTTP request. Considers, in order:
 *  - `?lang=xx` query parameter
 *  - the first `Accept-Language` tag with `q=` ignored (browsers send their
 *    preferred locale first which is good enough for our purposes)
 *  - English fallback.
 */
export function resolveBadgeI18nLangFromReq(req: Request): BadgeI18nLang {
  const queryLang = typeof req.query?.lang === "string" ? req.query.lang : null;
  if (queryLang) {
    const fromQuery = normalizeBadgeI18nLang(queryLang);
    if (fromQuery) return fromQuery;
  }
  const header = req.headers["accept-language"];
  if (typeof header === "string" && header) {
    // "fr-FR,fr;q=0.9,en;q=0.8" → "fr-FR"
    const first = header.split(",", 1)[0]?.split(";", 1)[0]?.trim();
    if (first) return normalizeBadgeI18nLang(first);
  }
  return "en";
}

