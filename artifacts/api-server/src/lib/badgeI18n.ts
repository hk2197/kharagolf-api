/** Translate a badge definition for the given locale. Falls back to English
 *  per-field so a partially-translated locale never displays a slug-like type. */
export function localizeBadge(def: BadgeDef, lang: BadgeI18nLang): BadgeI18nText {
  const en = T.en[def.type] ?? { label: def.label, description: def.description };
  if (lang === "en") return en;
  const localized = T[lang]?.[def.type];
  if (!localized) return en;
  return {
    label: localized.label || en.label,
    description: localized.description || en.description,
  };
}

